class Arthropod:
    def __init__(type, insect, arachnid):
        type.insect = insect
        type.arachnid = arachnid


class Geographical:
    def __init__(location, county, city):
        location.county = county
        location.city = city


class Glossary:
    def __init__(definition, culicidae):
        definition.culicidae = culicidae
